import { promises as fs } from "node:fs";
import path from "node:path";

const TASKS_ROOT = path.join(process.cwd(), "tasks");
const OUTPUT_FILE = path.join(
  process.cwd(),
  "src/lib/generated-task-preview-manifest.tsx",
);

function parseFrontmatter(content) {
  const match = content.match(/^---\n([\s\S]*?)\n---/);

  if (!match) {
    throw new Error("Missing frontmatter.");
  }

  const data = {};

  for (const line of match[1].split("\n")) {
    const trimmedLine = line.trim();

    if (!trimmedLine) {
      continue;
    }

    const separatorIndex = trimmedLine.indexOf(":");

    if (separatorIndex === -1) {
      throw new Error(`Invalid frontmatter line: "${trimmedLine}"`);
    }

    const key = trimmedLine.slice(0, separatorIndex).trim();
    const value = trimmedLine.slice(separatorIndex + 1).trim();
    data[key] = value;
  }

  return data;
}

function toIdentifier(slug) {
  return slug
    .split(/[^a-zA-Z0-9]/g)
    .filter(Boolean)
    .map((part) => `${part.slice(0, 1).toUpperCase()}${part.slice(1)}`)
    .join("");
}

function getImportPath(category, slug, previewEntry) {
  const withoutExtension = previewEntry.replace(/\.[jt]sx?$/, "");
  return `../../tasks/${category}/${slug}/${withoutExtension}`.replace(/\\/g, "/");
}

async function collectPreviewTasks() {
  const categories = await fs.readdir(TASKS_ROOT, { withFileTypes: true });
  const tasks = [];

  for (const categoryEntry of categories) {
    if (!categoryEntry.isDirectory()) {
      continue;
    }

    const category = categoryEntry.name;
    const categoryPath = path.join(TASKS_ROOT, category);
    const entries = await fs.readdir(categoryPath, { withFileTypes: true });

    for (const entry of entries) {
      if (!entry.isDirectory()) {
        continue;
      }

      const slug = entry.name;
      const taskFilePath = path.join(categoryPath, slug, "task.md");
      let taskFile;

      try {
        taskFile = await fs.readFile(taskFilePath, "utf8");
      } catch (error) {
        if (error?.code === "ENOENT") {
          continue;
        }

        throw error;
      }

      const frontmatter = parseFrontmatter(taskFile);
      const hasPreview = frontmatter.hasPreview === "true";

      if (!hasPreview) {
        continue;
      }

      const previewEntry = frontmatter.previewEntry;

      if (!previewEntry) {
        throw new Error(`Missing previewEntry for ${taskFilePath}`);
      }

      tasks.push({ category, slug, previewEntry });
    }
  }

  return tasks.sort((left, right) => left.slug.localeCompare(right.slug));
}

function buildManifest(previewTasks) {
  const importLines = previewTasks.map((task) => {
    const identifier = `Preview${toIdentifier(task.slug)}`;
    const importPath = getImportPath(task.category, task.slug, task.previewEntry);
    return `import ${identifier} from "${importPath}";`;
  });

  const caseLines = previewTasks.map((task) => {
    const identifier = `Preview${toIdentifier(task.slug)}`;
    return `    case "${task.slug}":\n      return <${identifier} />;`;
  });

  const slugList = previewTasks.map((task) => `"${task.slug}"`).join(",\n  ");

  return `/* This file is auto-generated by scripts/generate-preview-manifest.mjs. */
${importLines.join("\n")}

export const previewTaskSlugs = [
  ${slugList}
] as const;

export function renderTaskPreviewFromManifest(slug: string) {
  switch (slug) {
${caseLines.join("\n")}
    default:
      return null;
  }
}
`;
}

async function main() {
  const previewTasks = await collectPreviewTasks();
  const manifest = buildManifest(previewTasks);
  await fs.writeFile(OUTPUT_FILE, manifest, "utf8");
  console.log(`Generated preview manifest for ${previewTasks.length} task(s).`);
}

void main();
